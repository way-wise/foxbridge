        <h3>Frost Delays: Six Things Every Golfer Should Know</h3>
        
        <ol type="i" class="listobjective1">
        <li>When the golf course runs a frost delay this normally pushes back all the tee times until the ground has warmed up enough to melt the frost.</li> 
        <li>Golf course turf is normally resilient to traffic, but when ice crystals form inside the plants, they become brittle and vulnerable to damage. Walking or driving over frost-covered grass may rupture plant cells, leading to dead turf. The plants may be weakened without immediately showing the effects. It can take grass more than a month to recover from this damage. </li>
        <li>Many of us have looked out our windows at home and seen no signs of frost, only to find a frost delay when we reach the golf course. This is because frost can linger in colder microclimates long after other areas have thawed. If you are worried about a potential frost delay it is best to call the Proshop to check on conditions at the course before leaving home.  </li>
        <li>Frost damage can occur on any part of the golf course, but it poses the greatest risk to closely mown turf. Putting greens are particularly vulnerable because they experience the most concentrated traffic. A foursome typically takes 300 steps or more on each putting green; if there is frost present, all those steps could cause serious damage.</li>
        <li>No signs of frost on the first tee? That doesn’t mean you will get the “all clear” sign. If frost remains in areas that are unavoidable early in the round, the course must remain closed. It is also important to remember that once the frost is totally clear, the maintenance staff will need time to catch up on course preparations before play can begin. </li>
        <li>Shade extends frost delays by preventing sunlight from melting the frost. Pruning or removing trees that shade primary playing surfaces can improve the course’s overall health and reduce the duration of frost delays. This is especially true on holes played early in the round. </li>
